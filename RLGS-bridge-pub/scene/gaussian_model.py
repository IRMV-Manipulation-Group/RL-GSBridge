                        w = gaussian_3d_coeff_mat(g_pts[:, start:end].reshape(-1, 3), g_covs[:, start:end].reshape(-1, 3, 3)).reshape(pts.shape[0], -1) # [M, l]
                        val += (mask_opas[:, start:end] * w).sum(-1)
